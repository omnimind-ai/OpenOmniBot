      return PlatformDispatcher.instance.locale;
    }
  }

  static AppLocalizations _localizationsFor(Locale locale) {
    return locale.languageCode == 'en' ? _enLocalizations : _zhLocalizations;
  }

  static String text(String text, {Locale? locale}) {
    final targetLocale = locale ?? _resolvedLocale;
    final exact = _exactText[text];
    if (exact != null) {
      return exact(_localizationsFor(targetLocale));
    }
    if (targetLocale.languageCode != 'en') {
      return text;
    }
    for (final (pattern, rewrite) in _regexEn) {
      final match = pattern.firstMatch(text);
      if (match != null) {
        return rewrite(match);
      }
    }
    return text;
  }

